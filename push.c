/* PUSH_X

Description: Push the top element of stack 'y' to stack 'x'.

Operations:
	1. Create a temporary pointer that's a copy of stack 'y' head;
	2. Create a temporary pointer that's a copy of stack 'x' head;
	3. Create a temporary pointer that's a copy of stack 'x' first_node->next;
	4. Make 'y' head be same as 'y' first_node->next;
	5. Make 'x' head be same as 'y' head;
	6. Make 'y' first_node->next be same as 'x' head; */

/* PUSH_A

Description: Use push function to stack 'a';

Operations:
	1. Call push function to stack 'a'; */

